    articles = ARTICLES.get(category, [])

    if not articles:
        await callback.message.answer("В этой категории пока нет статей.")
        await callback.answer()
        return

    kb = articles_list_keyboard(category, articles, 0)
    cat_name = CATEGORY_NAMES.get(category, category)
    text = f"📚 <b>{cat_name}</b>\n\nВыберите статью:"
